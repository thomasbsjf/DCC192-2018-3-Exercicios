@WebServlet(name = "ValidaServlet", urlPatterns = {"ValidaServlet", "index.jsp"})
public class ValidaServlet extends HttpServlet {

    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        response.setContentType("text/html;charset=UTF-8");

        String opcao = request.getParameter("opcao");

        switch (opcao) {
            case "login":
                IndexCommand index = new IndexCommand();
                index.exec(request, response);
                break;

            case "listar":
                ListarUsuarioCommand listaUsuario = new ListarUsuarioCommand();
                listaUsuario.exec(request, response);
                break;

            default:
                response.sendRedirect("erro.html");
        }
    }

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }
}
